                        &nbsp;&nbsp;&nbsp;&nbsp;Eu estava hospedada na casa de uma amiga em uma cidade conhecida por ter muitas casas antigas assombradas. Ela é uma ateia, uma médica, muito inteligente e muito céptica, então quando ela me disse diretamente: "Eu adoraria que você me visitasse, mas aviso logo, minha casa está assombrada", eu fiquei tipo "...?" Mas eu a conheço desde que eu estava literalmente no útero (temos quase exatamente a mesma idade e nossas mães são amigas íntimas e costumavam andar juntas constantemente, assim como nós) então se ela disse isso, eu acreditei nela.
                        <br/><br/>
                        Ela me relatou uma série de incidentes que eu não testemunhei pessoalmente, como móveis movendo-se sozinho em uma sala trancada, coisas sendo violentamente derrubadas e jogadas, seus cães se recusando a ir ao porão, mesmo vendo uma figura humana semi-transparente quando ela encostou na entrada de sua garagem uma noite.
                        <br/><br/>
                        Por ser uma casa bem pequena, não é muito à prova de som, por isso é fácil saber onde alguém está. Geralmente se entra pela porta lateral, e esta se abre para um longo corredor. Porta do porão à esquerda, porta do quarto à direita, cozinha à esquerda, sala de estar e sala de jantar à direita, banheiro na outra extremidade da casa. A sala de jantar estava separada da sala de estar por uma parede, e tinha sido a fonte da maioria das coisas de que ela me relatou.
                        <br/><br/>
                        Então, na primeira noite em que lá estou, tudo está bem, dizemos boa noite, ela entra no quarto com seus cães e fecha a porta do quarto. Eu estou me preparando para dormir na sala de estar. De repente, ouço a porta lateral se abrindo e a janela se fechando. Eu pulo e me levanto rapidamente, o que quer que seja, talvez haja apenas uma brisa forte.
                        <br/><br/>
                        Então começam os malditos passos. Estou sentada perfeitamente imóvel no sofá. Minha amiga é uma moça de pequeno porte(cerca de 46kg). Seus cães são de tamanho médio (10-12). E eu começo a ouvir estes passos FORTES e LARGOS, como se uma pessoa enorme usando botas realmente pesadas estivesse andando lentamente pelo corredor. Por perspectiva, na época eu estava em uma relação com alguém que pesava cerca de 120kg e usava botas de biqueira de aço a maior parte do tempo, e até mesmo ele não fazia tanto barulho. Mas eles soam exatamente como passos, e definitivamente estão descendo o corredor.
