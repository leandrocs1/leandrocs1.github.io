                        Finalmente chegam à entrada da sala de estar, que ficava quase diretamente em frente à porta do quarto. O barulho continua acontecendo, mas fica parado praticamente em um lugar, e tenho a sensação distinta de que o que quer que fosse, está se virando para olhar para a porta fechada do quarto. Naquele momento, olhei finalmente para cima, e havia uma enorme sombra no meio do salão, ocupando toda a largura do mesmo e caindo sobre a porta do quarto. Não sei como descrevê-la, mas realmente parecia que eu estava olhando para as costas de alguma criatura sombria, mas muito sólida.
                        <br/><br/>
                        Não sou uma pessoa religiosa ou espiritual, em geral, mas basicamente tenho TOC (o que me torna muito supersticiosa, por falta de um termo melhor, particularmente fixando-me na ideia de que algo que faço ou não faço causará danos às pessoas que amo). Não quero falar muito sobre isso porque não é esse o objetivo, mas por puro instinto, fiz algumas coisas de muita 'boa fé', e me lembro de não poder olhar para cima da mesa do café, sentindo que algo estava empurrando minha cabeça para baixo, então pensei: "Vá embora, deixe-a em paz, vá embora, pegue alguém do seu próprio tamanho", uma e outra vez para o que me pareceu uma eternidade. Então a pressão levantou, e eu olhei para cima, e ela tinha desaparecido. Tive um ataque de pânico durante uma hora, finalmente me acalmei o suficiente para dormir, e o resto da visita foi sem problemas.
